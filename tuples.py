#Tuples

my_tuple = (1, 2, 3, 4, 5)

print(my_tuple)
print(my_tuple[0])
print(my_tuple[2])
print(my_tuple[-1]) #prints the last item in the tuple

tuple_1 = (1, 2, 3)
tuple_2 = (4, 5, 6)

conc_tuple = tuple_1 + tuple_2 #combines multiple tuples
rep_tuple = tuple_1 * 3 #repeats a tuple

print(conc_tuple) 
print(rep_tuple) 